def fetch():
    rows = []
    for code, name in NIKKEI225:
        info = None
        fin = None
        # yfinance はまれに一時的なエラーを返すため、最大3回リトライする
        for attempt in range(3):
            try:
                t = yf.Ticker(code)
                info = t.info
                fin = t.financials  # 損益計算書（列=決算期）
                break
            except Exception as e:
                if attempt < 2:
                    time.sleep(1.5)
                else:
                    print(f"NG  {name} ({code}): {e}")

        if info is None:
            rows.append({"code": code, "name": name})
            time.sleep(0.2)
            continue

        # 成長率計算（最新 vs 1期前）
        def growth(metric):
            if fin is not None and metric in fin.index:
                s = fin.loc[metric].dropna()
                if len(s) >= 2:
                    return (s.iloc[0] / s.iloc[1] - 1) * 100
            return None

        rev_g = growth("Total Revenue")
        op_g = growth("Operating Income")

        rows.append({
            "code": code,
            "name": name,
            "price": info.get("currentPrice"),
            "market_cap": info.get("marketCap"),
            "per": info.get("trailingPE"),
            "pbr": info.get("priceToBook"),
            "div_yield": info.get("dividendYield"),
            "revenue_growth": rev_g,
            "operating_profit_growth": op_g,
            "sector": info.get("sector"),
        })
        rev_s = f"{rev_g:.1f}%" if rev_g is not None else "NA"
        per_s = info.get("trailingPE")
        print(f"OK  {name}: 売上成長={rev_s} PER={per_s}")
        time.sleep(0.2)  # 負荷軽減

    df = pd.DataFrame(rows)
    df.to_csv("japan_stocks.csv", index=False, encoding="utf-8-sig")
    ok = df["price"].notna().sum()
    print(f"\n保存完了: {len(df)} 社中 {ok} 社のデータ取得成功 -> japan_stocks.csv")
    return df


if __name__ == "__main__":
    fetch()
